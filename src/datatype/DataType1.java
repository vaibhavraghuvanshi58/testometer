package datatype;

public class DataType1 {
	public static void main(String[] args) {
		
//		small cases letter  - keywords
//      variable - vary + able
// constant = final
		
		// casting
		
		int i = 0X123;  // octal 
//		System.out.println(i);
		
		
		// When ever we add 0 before any number - it become octal
		// When ever we add 0 before any number - it become hexadecimal
		
		
		// Floating Value 
		float f1 = 12.6f;
		double d1 = 15.7d;
		
		boolean h = true;
		boolean k = false;
		
		char c = '5';
		System.out.println(c); // 5
		int m  = (int)c;
		System.out.println(m); //53
		// you can convert int to char and char to int
		char df = 99;
		System.out.println(df);
	}
}
